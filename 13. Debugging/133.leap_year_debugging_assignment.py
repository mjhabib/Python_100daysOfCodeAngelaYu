year = int(input("Type a year: "))  # originally we did not turn our input into integer which we got a TypeError message

if year % 4 == 0:
    if year % 100 == 0:
        if year % 400 == 0:
            print("Leap year")
        else:
            print("Not leap year")
    else:
        print("Leap year")
else:
    print("Not leap year")


