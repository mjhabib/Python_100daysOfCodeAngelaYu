# while something_is_true:
#   do something repeatedly

counter = 5
while counter > 0:
    print("Hey")
    counter -= 1

true_false = True
while true_false:  # while true_false == True
    print("Hoy")
    true_false = False
# or
true_false = False
while not true_false:  # while true_false != True
    print("Hehe")
    true_false = True
